 function [] = d1_disp(bp, wls, user)



%------------------------------Turn on Diary-------------------------------
 d5_diary_on(bp, user)
%--------------------------------------------------------------------------


%---------------------------Estimation Terminal----------------------------
 d2_estimation(bp, wls, user.stop)
%--------------------------------------------------------------------------


%------------------------Spectral Radius Terminal--------------------------
 if user.radius == 1
    d3_spectral(bp)
 end
%--------------------------------------------------------------------------


%------------------State Estimation Evaluation Terminal--------------------
 if user.error == 1
    d4_evaluation(wls, bp)
 end
%--------------------------------------------------------------------------


%------------------------------Turn off Diary------------------------------
 d6_diary_off(user)
%--------------------------------------------------------------------------